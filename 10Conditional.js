/*
    1. if-else if-else Statement
     
    SYNTAX:

    if (condition1) 
    {
       // code to execute if condition1 is true
    } 
    else if (condition2) 
    {
       // code to execute if condition2 is true
    } 
    else 
    {
       // code to execute if none of the conditions are true
    }



    ex-
    let score = 75;
    if (score > 90) 
    {
       console.log("Grade: A");
    }
    else if (score > 75) 
    {
        console.log("Grade: B");
    } 
    else 
    {
        console.log("Grade: C");
    }

*/





